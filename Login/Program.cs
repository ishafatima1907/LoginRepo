// Add authentication services
builder.Services.AddAuthentication(options =>
{
    options.DefaultAuthenticateScheme = CookieAuthenticationDefaults.AuthenticationScheme;
    options.DefaultChallengeScheme = GoogleDefaults.AuthenticationScheme;
    options.DefaultSignInScheme = CookieAuthenticationDefaults.AuthenticationScheme; // Explicitly setting DefaultSignInScheme
})
.AddCookie(CookieAuthenticationDefaults.AuthenticationScheme, options =>
{
    options.LoginPath = "/auth/login"; // Ensuring login path is set, adjust accordingly
    options.LogoutPath = "/auth/logout"; // Ensuring logout path is set, adjust accordingly
})
//.AddGoogle(options =>
